#include "libcli.h"
#include "graph.h"
#include <stdio.h>

extern
graph_t *graph;

static int
show_graph_handler(param_t *param, ser_buff_t *b, op_mode enable_or_disable){
    
    printf("%s() is called .. graph = 0x%x\n", __FUNCTION__, graph);
    return 0;
}

void
spf_init_dcm(){
    
    init_libcli();

    param_t *show   = libcli_get_show_hook();
    param_t *debug  = libcli_get_debug_hook();
    param_t *config = libcli_get_config_hook();

    static param_t graph;
    init_param(&graph, CMD, "graph", show_graph_handler, 0, INVALID, 0, "Network graph");
    libcli_register_param(show, &graph);

    support_cmd_negation(config);
}
